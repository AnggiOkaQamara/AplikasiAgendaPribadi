    private void EditButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditButtonActionPerformed
       int i = AgendaTable.getSelectedRow();
       TableModel dtagenda = AgendaTable.getModel();
       
       String Hari = HariComboBox.getSelectedItem().toString();
        String Waktu = WaktuComboBox.getSelectedItem().toString();
        String Agd = textAgenda.getText();

      int listdata = 0;
            for (Agenda bk: Pribadi){
                if (bk.getHari().equals(Hari)) {
                    listdata = Pribadi.indexOf(bk);
                    break;
                }
            }
      Pribadi.set(listdata,new Agenda(Hari, Waktu, Agd));
       RefreshData();
       

    }//GEN-LAST:event_EditButtonActionPerformed

