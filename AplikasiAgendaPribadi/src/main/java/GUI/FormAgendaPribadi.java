    public void tampilTabel (){
        ArrayList<Agenda>listdata = new ArrayList<Agenda>();
        listdata=Pribadi;
        DefaultTableModel  dtagenda = (DefaultTableModel) AgendaTable.getModel();
        Object[] row = new Object[3];
        for (int i = 0; i < listdata.size(); i++) {
            row[0] = listdata.get(i).getHari();
            row[1] = listdata.get(i).getWaktu();
            row[2] = listdata.get(i).getAgd();
            dtagenda.addRow(row);
        }
        
} 
